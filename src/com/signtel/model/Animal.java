package com.signtel.model;

public class Animal {

	String walk;
	String sing;
	String swim;
	String fly;
	boolean walkFlag;
	boolean singFlag;
	boolean swimFlag;
	boolean flyFlag;
	
	public String getWalk() {
		return walk;
	}
	public void setWalk(String walk) {
		this.walk = walk;
	}
	public String getSing() {
		return sing;
	}
	public void setSing(String sing) {
		this.sing = sing;
	}
	public String getSwim() {
		return swim;
	}
	public void setSwim(String swim) {
		this.swim = swim;
	}
	public String getFly() {
		return fly;
	}
	public void setFly(String fly) {
		this.fly = fly;
	}
}
